def most_spoken_languages(filename, n):
    most_spoken = {}
    for country in filename:
        for key, value in country.items():
            if key == 'languages':
                for language in value:
                    if language not in most_spoken:
                        most_spoken[language] = 1
                    else:
                        most_spoken[language] += 1
    most_spoken = sorted(most_spoken.items(), key=lambda kv:kv[1], reverse=True)[:n]
    return most_spoken
    
